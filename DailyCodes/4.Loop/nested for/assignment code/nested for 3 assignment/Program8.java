class Program8{
	public static void main(String[]args){


		int rowcol=4;
		
		char ch='J';
			
		for(int i=1;i<=rowcol;i++){
	
						
			for(int j=1;j<=i;j++){  
				System.out.print(ch +"   ");
				ch--;
			}

		
		System.out.println();
		}
	}
}




