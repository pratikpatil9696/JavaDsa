class Unary{

public static void main(String[]args){
int x=10;
int y=20;

System.out.println("Pre Increament");

System.out.println(++x);
System.out.println(++y);


System.out.println(--x);
System.out.println(--y);


System.out.println(x);
System.out.println(y);

System.out.println("Post Increament");

System.out.println(x++);
System.out.println(y--);


System.out.println(x);
System.out.println(y);



}


}